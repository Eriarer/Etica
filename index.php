    <p> Las posibles causas que se relacionan con el síndrome del impostor son:</p>
    <ul>
      <li>Las experiencias de comparación y sobrestimación durante la infancia, como por ejemplo en el caso de padres que presionan demasiado a sus hijos para que saquen buenas notas.</li>
      <li>Los estereotipos sexuales, debido a los mensajes recibidos sobre el éxito en los hombres y el fracaso en las mujeres.</li>
      <li>Las altas expectativas y la autoexigencia.</li>
      <li>La baja autoestima.</li>
      <li>Falta de confianza en las propias habilidades.</li>
      <li>La pérdida de confianza en uno mismo.</li>
