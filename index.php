    <h3>Estadísticas y prevalencia</h3>
    <p>Un estudio de 2019 realizado por la Universidad de York encontró que el síndrome del impostor es más común de lo que se pensaba anteriormente. El estudio, que se basó en una encuesta a más de 10.000 personas de todo el mundo, encontró que el 70% de los encuestados había experimentado al menos algunos síntomas del síndrome del impostor en algún momento de sus vidas.</p>
    <p>El síndrome del impostor es más común en profesiones que requieren una alta competencia, como la ciencia, la tecnología, la ingeniería y las matemáticas (STEM), así como en profesiones que requieren una alta exposición pública, como la actuación, la música y el deporte.</p>

